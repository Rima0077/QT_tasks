#include <iostream>

int main()
{
	int Integer = 7;
	float Float = 7.7;
	double Double = 17.7;
	char Char = 'R';
	bool Bool = true;

	std::cout << "Integer: " << Integer << std::endl;
	std::cout << "Float: " << Float << std::endl;
	std::cout << "Double: " << Double << std::endl;
	std::cout << "Character: " << Char << std::endl;
	std::cout << "Boolean: " << Bool << std::endl;

	return 0;
} 
